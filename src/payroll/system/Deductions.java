    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:

        int p = JOptionPane.showConfirmDialog(null, "Are you sure you want to add record?", "Add Record", JOptionPane.YES_NO_OPTION);
        if (p == 0) {

            String value3 = lblEmp.getText();
            try {

                String sql = "INSERT INTO deduction (Firstname,Lastname,Deduct_Amount,Deduct_Reason,Emp_Id,Salary,Created_by) values (?,?,?,?,?,?,'" + value3 + "')";
                pst = con.prepareStatement(sql);
                pst.setString(1,jtxtFN.getText());
                pst.setString(2,jtxtLN.getText());
                pst.setString(3,lblTotal.getText());
                pst.setString(4,jtxtReas.getText());
                pst.setString(5,jtxtID.getText());
                pst.setString(6,jtxtBasicS.getText());

                pst.execute();
                JOptionPane.showMessageDialog(null, "Data is saved successfully");

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
            finally{
                try{
                    rs.close();
                    pst.close();
                }catch(Exception e){
                   JOptionPane.showMessageDialog(null, e); 
                }
            }
  
        }

    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int salary = Integer.parseInt(jtxtBasicS.getText());
        if(rb_perc.isSelected() == true){
            int percentage = Integer.parseInt(jtxt_per.getText());
            int total_percentage_deduction = salary /100 * percentage;
            String x = String.valueOf(total_percentage_deduction);
            int sal = salary - total_percentage_deduction;
            lblTotal.setText(x);
            lblSal.setText(String.valueOf(sal));
        } 
        if(rb_am.isSelected() == true){
            int deductions = Integer.parseInt(jtxt_Am.getText());
            int total_amount_deduction = salary - deductions;
            String s = String.valueOf(total_amount_deduction);
           
            
            lblSal.setText(s);
            lblTotal.setText(String.valueOf(deductions));
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

