    private void jtxtSearchIDKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtxtSearchIDKeyReleased
        con = ConnectDB();
        String sql = "select * from employee_record where Emp_Id=?";
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            try {

                pst = con.prepareStatement(sql);
                pst.setString(1, jtxtSearchID.getText());
                rs = pst.executeQuery();
                if (rs.next()) {

                    String puts1 = rs.getString("Emp_Id");
                    jtxtID.setText(puts1);

                    String puts2 = rs.getString("Firstname");
                    jtxtFN.setText(puts2);

                    String puts3 = rs.getString("Lastname");
                    jtxtLN.setText(puts3);

                    String puts4 = rs.getString("Dob");
                    jtxtDOB.setText(puts4);

                    String puts5 = rs.getString("Designation");
                    jtxtDisg.setText(puts5);

                    String puts6 = rs.getString("Basic_Salary");
                    jtxtBasicS.setText(puts6);

                    String puts7 = rs.getString("Date_hired");
                    jtxtDoH.setText(puts7);
                } else {
                    JOptionPane.showMessageDialog(null, "Employee does not exist!");
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            } finally {
                try {
                    rs.close();
                    pst.close();
                } catch (Exception e) {

                }
            }
        }

    }//GEN-LAST:event_jtxtSearchIDKeyReleased

